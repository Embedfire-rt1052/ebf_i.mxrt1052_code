#include "./bsp/gpt/bsp_gpt.h"    
    
    
/*Card结构描述符*/
sd_card_t g_sd;
volatile unsigned int i = 0; //用于控制RGB灯的翻转

/**
 * @brief 延时一段时间
 */
void delay(uint32_t count);

/**
 * @note 本函数在不同的优化模式下延时时间不同，
 *       如flexspi_nor_debug和flexspi_nor_release版本的程序中，
 *       flexspi_nor_release版本的延时要短得多  
 */ 
void delay(uint32_t count)
{   
    volatile uint32_t i = 0;
    for (i = 0; i < count; ++i)
    {
        __asm("NOP"); /* 调用nop空指令 */
    }
}


/**
  * @brief  主函数
  * @param  无
  * @retval 无
  */
int main(void)
{
    /* 初始化内存保护单元 */      
    BOARD_ConfigMPU();
    /* 初始化开发板引脚 */
    BOARD_InitPins();
    /* 初始化开发板时钟 */
    BOARD_BootClockRUN();
    /* 初始化调试串口 */
    BOARD_InitDebugConsole();
    
    /*设置中断优先级分组*/
    Set_NVIC_PriorityGroup(Group_4); 
    
    /* 打印系统时钟 */
    PRINTF("\r\n");
    PRINTF("*****欢迎使用 野火i.MX RT1052 开发板*****\r\n");
    PRINTF("CPU:             %d Hz\r\n", CLOCK_GetFreq(kCLOCK_CpuClk));
    PRINTF("AHB:             %d Hz\r\n", CLOCK_GetFreq(kCLOCK_AhbClk));
    PRINTF("SEMC:            %d Hz\r\n", CLOCK_GetFreq(kCLOCK_SemcClk));
    PRINTF("SYSPLL:          %d Hz\r\n", CLOCK_GetFreq(kCLOCK_SysPllClk));
    PRINTF("SYSPLLPFD0:      %d Hz\r\n", CLOCK_GetFreq(kCLOCK_SysPllPfd0Clk));
    PRINTF("SYSPLLPFD1:      %d Hz\r\n", CLOCK_GetFreq(kCLOCK_SysPllPfd1Clk));
    PRINTF("SYSPLLPFD2:      %d Hz\r\n", CLOCK_GetFreq(kCLOCK_SysPllPfd2Clk));
    PRINTF("SYSPLLPFD3:      %d Hz\r\n", CLOCK_GetFreq(kCLOCK_SysPllPfd3Clk));
    
    /*初始化并开启GPT定时器*/
    GPT_Config();
    PRINTF("定时器计数频率 %d \r\n",CLOCK_GetFreq(kCLOCK_IpgClk));
    
    /*初始化SD卡接口的GPIO引脚*/
    USDHC1_gpio_init();
    USDHC_Host_Init(&g_sd);
    /*SD卡读、写测试函数，内部包含了SD卡的初始化*/
    SD_Card_Init(&g_sd);
    
    /*确定初始值为0*/
    PRINTF("确定初始值为0：%d \r\n",GPT2->CNT);
    while(1)
    {
      HD_write_text(&g_sd);//开始写入
      
      PRINTF("秒： %d \r\n",i);
      PRINTF("Timer Counter %d \r\n",GPT2->CNT);
      
      /*清零定时器*/
//      GPT2->CNT = 0;
      /*清零计数值*/
      i = 0;
      
      /*确定初始值为0*/
      PRINTF("确定初始值为0：%d \r\n",GPT2->CNT);
      HD_read_text(&g_sd);
        
      PRINTF("秒： %d \r\n",i);
      PRINTF("Timer Counter %d \r\n",GPT2->CNT);
      
      
      
      
      while(1);
      

      //SD_Card_Test(&g_sd);
      delay(0x3ffffff);
    }			
}
